        <h2 class="heading" id="communitycenter">About & Around Our Towns</h2>

        <p class="seperate">There are adventures as well as serene places to be found throughout our greater Rochester area. Lakes, rivers and the Erie Canal are a significant source of the history and housing story of the area. One of the Great Lakes, Ontario, has a big, vivacious personality to the north and the beautiful Finger Lakes are located to the south and east. Communities, business and industry that grew up around the historic Erie Canal and Genesee River region weave a story of tradition and unfolding new opportunities. Scenes showing the essence of the communities are shown here in photos that I have taken in my local travels.</p>

        <img class="communityPics" src="sunflower.png" alt="Sunflowers" title="Sunflowers">

        <p class="seperate">This Sunflower field in Pittsford is no drive-by viewing. People stop for a sunny photo background that goes on for acres. I spoke with the farmer who told me the farm has been in his family for 200 years. They harvest the field in the fall and welcome visitors.</p>

        <img class="communityPics" src="ErieCanal.jpg" alt="Erie Canal" title="Erie Canal">

        <p class="seperate">Trails along the Erie Canal are a magnet for walkers, runners and bikers in Fairport, Pittsford, Brighton, Spencerport and other towns. On the water, rowers and boats cruise the still waters. Houses, land, and businesses border the canal which is an important artery of New York history.
        </p>

        <img class="communityPics" src="CanandaiguaLake.jpg" alt="Canandaigua Lake" title="Canandaigua Lake">

        <p class="seperate">Canandaigua Lake, one of the Finger Lakes, is a popular destination and address. For those fortunate to have a view over the lake, their gaze falls on signs of lake life - boats, docks, cottages and impressive houses on its shores. Newly rebranded New York Kitchen is here.</p>

        <img class="communityPics" src="CasaLargo.jpg" alt="Casa Largo" title="Casa Largo">

        <p class="seperate">A winery in winter offers its own charms shown at the Casa Largo Winery in Fairport shortly before harvest for its ice wines. Wineries in the Finger Lakes are experiencing growth and recognition for some grape varietals that do well in the climate and grounds around the lakes.</p>

        <img class="communityPics" src="LetchworthPark.jpg" alt="Letchworth Park" title="Letchworth Park">

        <p class="seperate">Letchworth Park is known as “The Grand Canyon of the East”. Early spring had waterfalls bursting from snow run-off and rains that will transition in summer and fall to lower river levels and glorious foliage that attracts thousands of visitors each year.</p>

        <img class="communityPics" src="LakeOntario.jpg" alt="Lake Ontario" title="Lake Ontario">

        <p class="seperate">Lake Ontario sunsets put on a new show everyday and this one was on fire on an August evening. Watching the lake is a favorite pastime for many that witness how the lake changes daily and seasonally. Truly a great lake.</p>

        <img class="communityPics" src="JohnDeere.jpg" alt="John Deere Combine" title="John Deere Combine">

        <p class="seperate">Agriculture is a backbone of western New York life as shown in this photo of a John Deere Harvester working the fields in Fairport. Locally grown produce is appealing for homes and restaurants.</p>

        <img class="communityPics" src="LibertyBell.jpg" alt="Liberty Bell" title="Liberty Bell">

        <p class="seperate">The John Deere iconic green and yellow-gold colors are well known for lawn care tractors and farm equipment. For several generations the Perry family owned and operated a John Deere franchise in western NY. Here is a replica of the Liberty Bell presented to our family by the John Deere company to recognize business and community service. It is dated 1776-1976 given for the U.S. Bicentennial and resides at my parent’s backyard in Niagara County.</p>

       <img class="communityPics" src="Howard_Hanna_rebranding.jpg" alt="Howard Hanna Rebranding Event" title="Howard Hanna Rebranding Event">

        <p class="seperate">My favorite color is red, however I bleed green and gold, first because of our family business heritage and now with my professional affiliation with Howard Hanna Real Estate Services. This picture was at an announcement event in July 2017 in Canandaigua where Nothnagle Realtors became Howard Hanna. Howard Hanna is ranked 1st for real estate firms in Rochester, 1st in NYS, and 3rd in the USA. </p>

